function renderIcon(name, { size = 24, color = 'currentColor', strokeWidth = 2, className } = {}) {
  const node = ICONS[name];
  if (!node) return '';
  const shapes = node
    .map(([tag, attrs]) => {
      const attrStr = Object.entries(attrs)
        .filter(([k]) => k !== 'key')
        .map(([k, v]) => `${k}="${escapeAttr(v)}"`)
        .join(' ');
      return `<${tag} ${attrStr}></${tag}>`;
    })
    .join('');
  const classAttr = className ? ` class="${escapeAttr(className)}"` : '';
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="${escapeAttr(color)}" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round"${classAttr}>${shapes}</svg>`;
}

